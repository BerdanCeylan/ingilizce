def get_episode_title(series_id: str, season: int, episode: int) -> str:
    """BÃ¶lÃ¼m baÅŸlÄ±ÄŸÄ±nÄ± getir"""
    episodes = get_episodes(series_id, season)
    if episodes and 1 <= episode <= len(episodes):
        return episodes[episode - 1]
