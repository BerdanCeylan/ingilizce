def build_episode_list(series_id: str) -> list:
    """TÃ¼m bÃ¶lÃ¼mleri dÃ¼z liste olarak getir"""
    result = []
    counts = get_episode_counts(series_id)
    
    for season, ep_count in counts.items():
        for ep in range(1, ep_count + 1):
            result.append({
                "season": season,
                "episode": ep,
                "title": get_episode_title(series_id, season, ep)
            })
    
    return result

